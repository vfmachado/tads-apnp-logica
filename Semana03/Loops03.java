
public class Loops03 {
    
    public static void main(String[] args) {

        System.out.println("while...");
        boolean meuFalso = false;
        while (meuFalso) {
            System.out.println("estou no while");
        }
        System.out.println("===================");


        System.out.println("do ... while");
        do {
            System.out.println("Estou no do...while");
        } while (meuFalso);
    
    }


}
